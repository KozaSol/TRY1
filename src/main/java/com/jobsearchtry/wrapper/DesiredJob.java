package com.jobsearchtry.wrapper;

public class DesiredJob {
    private String jobseeker_id, location, shift_type, salary, role, job_type, industry,
            desiredlocation_local, desired_role_name_local, desired_industry_local, desired_jobtype_local;

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getJobseeker_id() {
        return jobseeker_id;
    }

    public void setJobseeker_id(String Jobseeker_id) {
        jobseeker_id = Jobseeker_id;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String Location) {
        location = Location;
    }

    public String getShift_type() {
        return shift_type;
    }

    public void setShift_type(String Shift_type) {
        shift_type = Shift_type;
    }

    public String getSalary() {
        return salary;
    }

    public void setSalary(String Salary) {
        salary = Salary;
    }

    public String getJob_type() {
        return job_type;
    }

    public void setJob_type(String Job_type) {
        job_type = Job_type;
    }

    public String getIndustry() {
        return industry;
    }

    public void setIndustry(String Industry) {
        industry = Industry;
    }

    public String getDesiredlocation_local() {
        return desiredlocation_local;
    }

    public void setDesiredlocation_local(String desiredlocation_local) {
        this.desiredlocation_local = desiredlocation_local;
    }

    public String getDesired_role_name_local() {
        return desired_role_name_local;
    }

    public void setDesired_role_name_local(String desired_role_name_local) {
        this.desired_role_name_local = desired_role_name_local;
    }

    public String getDesired_industry_local() {
        return desired_industry_local;
    }

    public void setDesired_industry_local(String desired_industry_local) {
        this.desired_industry_local = desired_industry_local;
    }

    public String getDesired_jobtype_local() {
        return desired_jobtype_local;
    }

    public void setDesired_jobtype_local(String desired_jobtype_local) {
        this.desired_jobtype_local = desired_jobtype_local;
    }
}
