package com.jobsearchtry.wrapper;

public class Education {
    private String education_id, Qualification_id, qualification_name, Specilisation, institute_name,
            course_type, passout_year, coursename_local;

    public String getEducation_id() {
        return education_id;
    }

    public void setEducation_id(String Education_id) {
        education_id = Education_id;
    }

    public String getQualification_id() {
        return Qualification_id;
    }

    public void setQualification_id(String qualification_id) {
        Qualification_id = qualification_id;
    }

    public String getQualification() {
        return qualification_name;
    }

    public void setQualification(String Qualification_name) {
        qualification_name = Qualification_name;
    }

    public String getSpecilisation() {
        return Specilisation;
    }

    public void setSpecilisation(String specilisation) {
        Specilisation = specilisation;
    }

    public String getInstitute_name() {
        return institute_name;
    }

    public void setInstitute_name(String Institute_name) {
        institute_name = Institute_name;
    }

    public String getCourse_type() {
        return course_type;
    }

    public void setCourse_type(String Course_type) {
        course_type = Course_type;
    }

    public String getPassout_year() {
        return passout_year;
    }

    public void setPassout_year(String Passout_year) {
        passout_year = Passout_year;
    }

    public String getCoursename_local() {
        return coursename_local;
    }

    public void setCoursename_local(String coursename_local) {
        this.coursename_local = coursename_local;
    }
}
